package com.who.onecupafterwork.data;

import java.io.Serializable;

public class Profile implements Serializable {
	public String upid, upfacebook, upblood, upintro, upreligion, uphobby, upinterest, uparea; // intro는 매력한줄

	public String uphoto, uscore; // 좋아요 개수

	public Profile() {

	}

	public Profile(String upblood, String upreligion, String upinterest, String uphobby, String upfacebook, String upintro, String uparea) {
		this.upblood = upblood;
		this.upreligion = upreligion;
		this.upinterest = upinterest;
		this.uphobby = uphobby;
		this.upfacebook = upfacebook;
		this.upintro = upintro;
		this.uparea = uparea;
		this.uscore = "0";
	}

	public void setUscore(String uscore) {
		this.uscore = uscore;
	}

	public void setUphoto(String uphoto) {
		this.uphoto = uphoto;
	}

}
