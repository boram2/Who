package com.who.onecupafterwork.data;

public class MatchingGroup {

	public int matcher1Res; // 이성1, 주선자
	public int matcher2Res; // 이성2, 비주선자
	public String matchingDate;
	public String matchingLocation;
	// 사진이 나오는 사람들의 id가 필요 눌렀을 때 프로필 보기하려면
	public String matcherId1, matcherId2;

	public MatchingGroup(String mid1, String mid2, int res1, int res2, String date, String location) {
		matcherId1 = mid1;
		matcherId2 = mid2;
		matcher1Res = res1;
		matcher2Res = res2;
		matchingDate = date;
		matchingLocation = location;
	}
}
